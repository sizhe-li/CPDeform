def pack_seqdim(x, B):
    shapelist = list(x.shape)
    B_, S = shapelist[:2]
    assert (B == B_)
    other_dims = shapelist[2:]
    return x.view([B * S] + other_dims)


def unpack_seqdim(x, B):
    shapelist = list(x.shape)
    BS = shapelist[0]
    assert (BS % B == 0)
    other_dims = shapelist[1:]
    S = int(BS / B)
    return x.view([B, S] + other_dims)


